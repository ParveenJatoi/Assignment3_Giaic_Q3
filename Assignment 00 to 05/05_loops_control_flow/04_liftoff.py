def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Start at 10, stop before 0, step -1
        print(i)
    
    # Print the final call
    print("Liftoff!")


# This provided line is required at the end of
# Python file to call the main() function.
if __name__ == '__main__':
    main()
